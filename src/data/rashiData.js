export const getRashiByMonth = (month, day) => {
  const rashiMonths = {
    'Aries': [3, 21, 4, 19],
    'Taurus': [4, 20, 5, 20],
    'Gemini': [5, 21, 6, 20],
    'Cancer': [6, 21, 7, 22],
    'Leo': [7, 23, 8, 22],
    'Virgo': [8, 23, 9, 22],
    'Libra': [9, 23, 10, 22],
    'Scorpio': [10, 23, 11, 21],
    'Sagittarius': [11, 22, 12, 21],
    'Capricorn': [12, 22, 1, 19],
    'Aquarius': [1, 20, 2, 18],
    'Pisces': [2, 19, 3, 20],
  }

  for (let [rashi, [startMonth, startDay, endMonth, endDay]] of Object.entries(
    rashiMonths
  )) {
    if (month === startMonth && day >= startDay) {
      return rashi
    }
    if (month === endMonth && day <= endDay) {
      return rashi
    }
  }

  return 'Capricorn'
}
